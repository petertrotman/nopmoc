<h1>Nopmoc</h1>
<h3>Component reverse lookup</h3>
<p>Find electronic components by filtering by pin count, package, pin names and types. Uses the KiCad symbol libraries so only components included there can be found here.</p>
<p>Visit <a href="https://kit.svelte.dev">kit.svelte.dev</a> to read the documentation</p>

